// Language System
let currentLang = localStorage.getItem('language') || 'tr';

// Translation function
function translatePage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            const translation = translations[lang][key];
            
            // If element has no children, simple text update
            if (element.children.length === 0) {
                element.textContent = translation;
            } else {
                // Element has children (like icons), update only text nodes
                const textNodes = [];
                const walker = document.createTreeWalker(
                    element,
                    NodeFilter.SHOW_TEXT,
                    null,
                    false
                );
                
                let node;
                while (node = walker.nextNode()) {
                    if (node.textContent.trim()) {
                        textNodes.push(node);
                    }
                }
                
                // Update first text node, remove others
                if (textNodes.length > 0) {
                    textNodes[0].textContent = translation;
                    textNodes.slice(1).forEach(n => n.textContent = '');
                } else {
                    // No text node, append translation as text
                    element.appendChild(document.createTextNode(translation));
                }
            }
        }
    });
    
    // Update active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    translatePage(currentLang);
    
    // Language button click handlers
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            translatePage(lang);
        });
    });
});

// Mobile Menu Toggle
